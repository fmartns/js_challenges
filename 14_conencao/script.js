// As regras para nomes de variáveis em JavaScript são as seguintes:

// 1 - Os nomes de variáveis devem começar com uma letra (a a z, ou A a Z), ou com o caractere underscore _, ou o cifrão $.

// 2 - Após a primeira letra, os nomes de variáveis podem conter letras, dígitos (0 a 9), underscores e cifrões.

// 3 - Os nomes de variáveis são sensíveis a maiúsculas e minúsculas. Isso significa que minhaVariavel 
// e minhavariavel são consideradas como duas variáveis diferentes.

// 4 - Não é permitido utilizar palavras reservadas (palavras-chave) do JavaScript como nomes de variáveis. 
// Por exemplo, let, var, const, if, else, for, function, entre outras, não podem ser usadas como nomes de variáveis.

// 5 - Os nomes de variáveis devem ser descritivos e representar o que a variável representa ou armazena, 
// para facilitar a leitura e o entendimento do código.

let exemplo1 = 'Exemplo com número';
let $exemplo = 'Exemplo com cifrão';
let _exemplo = 'Exemplo com underline';
let umExemplo = 'Exemplo em camelCase';

console.log(exemplo1);
console.log($exemplo);
console.log(_exemplo);
console.log(umExemplo);
