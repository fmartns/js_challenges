// Em JavaScript, existem algumas palavras reservadas 
// (também conhecidas como palavras-chave) que não podem ser usadas como nomes de variáveis, 
// pois possuem um significado especial na linguagem. Tentar usar essas palavras reservadas como nomes de variáveis resultará 
// em erros no código. Aqui estão algumas das palavras reservadas no JavaScript:

// var, let, const, if, else, switch, case, default, while, do, for, break, continue, return, function, this, new, class, extends, super, static, 
// typeof, instanceof, true, false, null, undefined, NaN, Infinity

// let if = 'Teste';
let ifTest = 'Teste';

// let function = 'Teste';
let functionTest = 'Teste';

console.log(ifTest);
console.log(functionTest);